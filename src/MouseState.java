public class MouseState {
    private boolean isMouseRunning;

    public MouseState(boolean isMouseRunning) {
        this.isMouseRunning = isMouseRunning;
    }

    public boolean getIsMouseRunning() {
        return isMouseRunning;
    }

    public void setIsMouseRunning(boolean mouseRunning) {
        isMouseRunning = mouseRunning;
    }
}
